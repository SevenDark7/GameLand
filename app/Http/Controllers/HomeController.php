        $recents = Game::query()->where([
            ['active', 1],
            ['status', 1]
        ])->latest()->limit(7)->get();

        $mosts = Game::query()->where([
            ['active', 1],
            ['status', 1]
        ])->latest('visit')->limit(7)->get();

        $blogs = Blog::query()->where([
            ['active', 1],
            ['status', 1]
        ])->latest()->limit(8)->get()->toArray();

        return view('home.home', compact('recents', 'mosts', 'blogs'));
    }

